package org.lig.hadas.aesop.qwGeneration.exceptions;

public class ActivityDescriptionException extends Exception {




	/**
	 * 
	 */
	private static final long serialVersionUID = -1042327520407990984L;
	public ActivityDescriptionException() { super(); }
	public ActivityDescriptionException(String message) { super(message); }
	public ActivityDescriptionException(String message, Throwable cause) { super(message, cause); }
	public ActivityDescriptionException(Throwable cause) { super(cause); }

}
